#include "so_long.h"

void	ft_map_dell(t_info *info)
{
	int	y;
	int	x;

	y = 0;
	while (y < info->rows)
	{
		x = 0;
		while (x < info->len)
		{
			info->map[y][x] = '0';
			x++;
		}
		y++;
	}
}
